#!/usr/bin/env python3
"""Generate packages.toml from components-<channel>.txt files.

Each components-<channel>.txt file contains one RPM NEVRA per line
(e.g. "curl-8.9.1-3.fc43" or "ImageMagick-1:7.1.1.47-3.fc43").

This script:
1. Creates base/packages.toml (if it doesn't exist) with a project-wide
   default publish channel of "none".
2. For each components-<channel>.txt, extracts the bare package names,
   creates a package group named "<channel>-packages", and sets its
   publish channel to "rpm-<channel>".

Usage:
    python3 scripts/generate-packages-toml.py components-base.txt components-build-only.txt
    python3 scripts/generate-packages-toml.py components-*.txt

The script is idempotent: running it again overwrites packages.toml.
"""

import argparse
import os
import re
import sys
from pathlib import Path


def extract_package_name(nevra: str) -> str:
    """Extract the package name from an RPM NEVRA string.

    Handles both epoch and non-epoch forms:
      curl-8.9.1-3.fc43           -> curl
      ImageMagick-1:7.1.1.47-3    -> ImageMagick
      389-ds-base-3.1.4-9.fc43    -> 389-ds-base
    """
    # RPM NEVRA: name-[epoch:]version-release[.dist]
    # Match name (greedy) then -[epoch:]version-release.
    # The version and release each start with a digit.
    match = re.match(r"^(.+)-(?:\d+:)?\d.*?-\d.*$", nevra)
    if match:
        return match.group(1)
    # Fallback: return as-is (shouldn't happen with valid NEVRA)
    return nevra


def parse_channel_from_filename(filepath: str) -> str:
    """Extract the channel name from a components-<channel>.txt filename."""
    basename = os.path.basename(filepath)
    match = re.match(r"^components-(.+)\.txt$", basename)
    if not match:
        sys.exit(f"Error: filename '{basename}' doesn't match 'components-<channel>.txt' pattern")
    return match.group(1)


def read_packages(filepath: str) -> list[str]:
    """Read package NEVRAs from a file and return sorted unique package names."""
    names = set()
    with open(filepath) as f:
        for line in f:
            line = line.strip()
            if not line or line.startswith("#"):
                continue
            names.add(extract_package_name(line))
    return sorted(names)


def channel_to_rpm_channel(channel: str) -> str:
    """Map a channel name to its rpm-<channel> form."""
    return f"rpm-{channel}"


def generate_packages_toml(channel_files: list[str], output_path: str) -> None:
    """Generate the packages.toml content and write it to output_path."""
    lines: list[str] = []

    # Header
    lines.append("#")
    lines.append("# Package publish configuration")
    lines.append(f"# Generated by {os.path.basename(__file__)}")
    lines.append("#")
    lines.append("")

    # Project-wide default: publish to "none"
    lines.append("# Project-wide baseline — unpublished unless overridden by a package group")
    lines.append("[default-package-config]")
    lines.append('publish = { channel = "none" }')
    lines.append("")

    # Process each channel file
    for filepath in sorted(channel_files, key=lambda f: parse_channel_from_filename(f)):
        channel = parse_channel_from_filename(filepath)
        rpm_channel = channel_to_rpm_channel(channel)
        group_name = f"{channel}-packages"
        packages = read_packages(filepath)

        if not packages:
            print(f"Warning: no packages found in {filepath}, skipping", file=sys.stderr)
            continue

        lines.append(f"# Packages published to {rpm_channel}")
        lines.append(f'[package-groups.{group_name}]')
        lines.append(f'description = "{channel} packages"')

        # Write packages array (one per line for readability)
        lines.append("packages = [")
        for pkg in packages:
            lines.append(f'    "{pkg}",')
        lines.append("]")
        lines.append("")

        lines.append(f"[package-groups.{group_name}.default-package-config.publish]")
        lines.append(f'channel = "{rpm_channel}"')
        lines.append("")

    # Write output
    output = "\n".join(lines)
    with open(output_path, "w") as f:
        f.write(output)

    print(f"Wrote {output_path} ({len(lines)} lines)")


def main():
    parser = argparse.ArgumentParser(
        description="Generate packages.toml from components-<channel>.txt files"
    )
    parser.add_argument(
        "files",
        nargs="+",
        help="One or more components-<channel>.txt files",
    )
    parser.add_argument(
        "-o",
        "--output",
        default=None,
        help="Output path for packages.toml (default: base/packages.toml relative to repo root)",
    )
    args = parser.parse_args()

    # Validate input files exist
    for f in args.files:
        if not os.path.isfile(f):
            sys.exit(f"Error: file not found: {f}")

    # Determine output path
    if args.output:
        output_path = args.output
    else:
        # Find repo root (directory containing azldev.toml)
        repo_root = Path(__file__).resolve().parent.parent
        if not (repo_root / "azldev.toml").exists():
            sys.exit("Error: cannot find repo root (azldev.toml). Use -o to specify output path.")
        output_path = str(repo_root / "base" / "packages.toml")

    generate_packages_toml(args.files, output_path)


if __name__ == "__main__":
    main()
